<script>
    document.addEventListener("DOMContentLoaded", function() {
        function fetchCart() {
            fetch('get_cart.php')
                .then(response => response.json())
                .then(data => {
                    renderCart(data);
                })
                .catch(error => console.error('Error fetching cart:', error));
        }

        function renderCart(cartItems) {
            const cartItemCount = document.querySelector('.cart-item_count');
            const cartItemWrapper = document.querySelector('.cart-item-wrapper');
            cartItemCount.textContent = cartItems.length;

            let cartHTML = '';
            let totalPrice = 0;

            cartItems.forEach(item => {
                totalPrice += item.price * item.quantity;
                cartHTML += `
                <div class="single-cart-item">
                    <div class="cart-img">
                        <a href="cart.php"><img src="${item.image}" alt=""></a>
                    </div>
                    <div class="cart-text">
                        <h5 class="title"><a href="cart.php">${item.name}</a></h5>
                        <div class="cart-text-btn">
                            <div class="cart-qty">
                                <span>${item.quantity}×</span>
                                <span class="cart-price">Rp${item.price}</span>
                            </div>
                            <button type="button" data-id="${item.id}" class="remove-item"><i class="ion-trash-b"></i></button>
                        </div>
                    </div>
                </div>
            `;
            });

            cartHTML += `
            <div class="cart-price-total d-flex justify-content-between">
                <h5>Total :</h5>
                <h5>Rp${totalPrice}</h5>
            </div>
            <div class="cart-links d-flex justify-content-between">
                <a class="btn product-cart button-icon flosun-button dark-btn" href="cart.php">View cart</a>
                <a class="btn flosun-button secondary-btn rounded-0" href="checkout.php">Checkout</a>
            </div>
        `;

            cartItemWrapper.innerHTML = cartHTML;
            attachRemoveItemListeners();
        }

        function attachRemoveItemListeners() {
            document.querySelectorAll('.remove-item').forEach(button => {
                button.addEventListener('click', function() {
                    const itemId = this.getAttribute('data-id');
                    removeFromCart(itemId);
                });
            });
        }

        function removeFromCart(id) {
            fetch('remove_from_cart.php', {
                    method: 'POST',
                    headers: {
                        'Content-Type': 'application/json'
                    },
                    body: JSON.stringify({
                        id
                    })
                })
                .then(response => response.json())
                .then(data => {
                    if (data.success) {
                        fetchCart();
                    } else {
                        console.error('Error removing item from cart:', data.message);
                    }
                })
                .catch(error => console.error('Error removing item from cart:', error));
        }

        fetchCart();
    });
</script>




